
//Write a program for a Single line comment, multi-line and documentation comments

class Q3 
{
	public static void main(String[] args)
	{
		
		//This is single line comment
		System.out.println("The single-line comment is used to comment only one line of the code.");
		//Note: Single line comments starts with two forward slashes (//).
		
		/*  
		This   
		is   
		multi line   
		comment  
		*/
		System.out.println("The multi-line comment is used to comment multiple lines of code.");
		//Note: Multi-line comments are placed between /* and */.
		
		/**  
		* Documentation Comments
		*We can use various tags to depict the parameter 
		*or heading or author name
		*We can also use HTML tags   
		* 
		*/    
		System.out.println("The documentation comment is used to create documentation API.");
		//Note: The documentation comments are placed between /** and */.
		
	}
}
