
//Define variables for different Data Types int, Boolean, char, float, double and print on the Console

class Q4
{
	public static void main(String[] args)
	{
		int i = 18277;			//variable i is defined for integer data type
		boolean b = true;		//variable b is defined for boolean data type
		char c = 'A';			//variable c is defined for character data type
		float f = 18.277f;		//variable f is defined for float data type
		double d = 277.0;		//variable d is defined for double data type
		
		System.out.println("Value of i: " + i);
		System.out.println("Value of b: " + b);
		System.out.println("Value of c: " + c);
		System.out.println("Value of f: " + f);
		System.out.println("Value of d: " + d);

	}
}
